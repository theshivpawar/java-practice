// Comparison / Relational Operators

package fundamentals.types;

public class ComparisonRelational {
    public static void main(String[] args) {

        int n1 = 10;
        int n2 = 20;

        System.out.println(n1 == n2);
        System.out.println(n1 != n2);
        System.out.println(n1 > n2);
        System.out.println(n1 >= n2);
        System.out.println(n1 < n2);
        System.out.println(n1 <= n2);

        int n3 = 20;
        int n4 = 20;

        System.out.println(n3 != n4);
        System.out.println(n3 <= n4);
        System.out.println(n3 >= n4);

    }
}
